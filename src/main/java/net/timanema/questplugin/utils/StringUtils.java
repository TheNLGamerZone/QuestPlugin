package net.timanema.questplugin.utils;

public class StringUtils
{
    public static String stripIncluding(String in, String start, boolean addDot)
    {
        return in.substring(in.indexOf(start) + (start + (addDot ? "." : "")).length());
    }

    public static boolean isUUID(String string)
    {
        return string != null && string.matches(
                "^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$");
    }
}
